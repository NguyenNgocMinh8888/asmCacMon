    private void LoadData(){
        listSP = spService.getList();
        defaultTBModel.setRowCount(0);
        for (SP sp : listSP) {
            defaultTBModel.addRow(new Object[]{
                sp.getMa(),
                sp.getTen(),
                sp.getHang(),
                sp.getGia(),
                sp.getTrangThai()
        });
    }
    }    
    private void LoadData1(){
        ghmds = gioHang.getList2();
        
        defaultTBModel1.setRowCount(0);
        for(GioHangMD d : ghmds ){
            if(d.getSoLuong()>=1){
                defaultTBModel1.addRow(new Object[]{
                    d.getMa(),
                    d.getTen(),
                    d.getHang(),
                    d.getSoLuong(),
                    d.getGia(),
                    d.getTrangThai()
                });
            }
        }
    }    
    public void doc(){
        File file = new File(filename);
        if (file.exists() == false) {
            JOptionPane.showMessageDialog(this, "File không tồn tại");
            return;
        }
        try {
            FileInputStream fis = new FileInputStream(file);
            ArrayList<SP> L;
            try (ObjectInputStream ois = new ObjectInputStream(fis)) {
                L = (ArrayList<SP>) ois.readObject();
            }
            this.spService.setList(L);
            this.LoadData();
        } catch (FileNotFoundException e) {
        } catch (IOException e) {
        } catch (ClassNotFoundException e) {
        }
    }
    
    

