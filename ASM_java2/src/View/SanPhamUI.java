    private void tblQLSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblQLSPMouseClicked
        int row = tblQLSP.getSelectedRow();
        SP sp = listSP.get(row);
        txtMa.setText(sp.getMa());
        txtTen.setText(sp.getTen());
        cbbHang.setSelectedItem(sp.getHang());
        txtGia.setText(String.valueOf(sp.getGia()));
        if(sp.getLoai()==1){
            rdoCu.setSelected(true);
        }else{
            rdoMoi.setSelected(true);
        }
        chkCon.setSelected(sp.getTrangThai());
        
        return;
    }//GEN-LAST:event_tblQLSPMouseClicked

    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        if(gioHang.getList2().isEmpty()){
            JOptionPane.showMessageDialog(this, "Yeu cau them san pham vao gio hang");
            return;
        }
        float gia = Float.valueOf(txtGia.getText());
        int soluong = Integer.parseInt(txtSoLuong.getText());
        float tongtien = soluong * gia;
        lblTongTien.setText(Float.valueOf(tongtien).toString());
        return;
        
    }//GEN-LAST:event_btnThanhToanActionPerformed

